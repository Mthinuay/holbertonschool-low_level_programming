#include "3-calc.h"

/**
 * op_add - Returns sum of two integers
 * @a: First int
 * @b: Second int
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Returns difference of two integers
 * @a: First int
 * @b: Second int
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Returns product of two integers
 * @a: First int
 * @b: Second int
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Returns quotient of two integers
 * @a: First int
 * @b: Second int
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Returns remainder of two integers
 * @a: First int
 * @b: Second int
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}

